import type { EntityId } from '../types';

/**
 * Forging recipes — combine gold + sacrificed items at a foundry-equipped
 * city to produce a target item. The cost in gold and required input items
 * is consumed; the new item is placed into the player's inventory (the
 * "lost items" pool at the foundry city, where any officer can equip it).
 *
 * Two families of recipe live here:
 *   • Utility designs (玄甲 / 七星燈 / 孟德新書 …) that refine an existing
 *     treasure/book/horse into a better one.
 *   • 鍛造專屬兵器 (forge-only weapons) — original arms that exist ONLY as a
 *     forge product (`item.forgeOnly`), so crafting them never duplicates the
 *     historical 名兵 an officer already holds or that can be found in the wild.
 */
export interface ForgeRecipe {
  id: EntityId;
  resultItemId: EntityId;
  goldCost: number;
  /** Required Foundry building level (0 = any foundry). */
  minFoundryLevel: number;
  /** Item IDs that must be in the same city's "lost items" pool to be sacrificed. */
  ingredients: EntityId[];
  /** 鐵料配方 — when set, this forge consumes this much of the city's **iron**
   *  stock instead of (or alongside) sacrificed items. Iron is renewable
   *  (smelted each season, tradable, haulable), so iron-cost recipes are not
   *  bounded by the finite pool of unique findable weapons. A recipe with an
   *  `ironCost` and empty `ingredients` costs only gold + iron. */
  ironCost?: number;
  description: string;
  descriptionZh?: string;
}

